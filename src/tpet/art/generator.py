def _generate_gemini_sprite_frames(config: TpetConfig, pet: PetProfile) -> tuple[list[Image.Image], Path]:
    """Generate and split a Gemini sprite sheet into individual frame images.

    Shared helper for the Gemini pixel-art and half-block pipelines.
    Handles API call, PNG save, sprite splitting, and per-frame PNG storage.

    Steps:
    1. Build prompt and generate sprite sheet via Gemini
    2. Split the sheet into individual frames (2x3 or 2x2 layout)
    3. Save each frame as a full-resolution PNG for runtime scaling

    Args:
        config: Application configuration.
        pet: Pet profile.

    Returns:
        Tuple of (list of PIL frame Images, Path to the sprite sheet PNG).

    Raises:
        RuntimeError: If the Gemini API call fails.
        OSError: If the sprite file cannot be read.
    """
    from tpet.art.client import GeminiImageClient
    from tpet.art.process import create_blink_frame, crop_frames_to_common_bbox, split_sprite_sheet

    art_dir = get_art_dir(config.pet_data_dir)
    sprite_path = art_dir / f"{_sanitize_name(pet.name)}_sprite.png"

    resolved = config.resolved_image_art_provider
    client = GeminiImageClient(model=resolved.model, api_key=resolved.api_key)
    prompt = build_sprite_prompt(pet, prompt_override=config.art_prompt)
    save_prompt(config.pet_data_dir, pet.name, prompt)
    logger.debug("=== Gemini sprite prompt (6-frame sheet) ===\n%s\n=== end prompt ===", prompt)
    client.generate_sprite(
        prompt=prompt,
        output_path=sprite_path,
        aspect_ratio="2:3",
        resolution="1k",
    )

    sprite = Image.open(sprite_path)
    logger.debug("Sprite sheet loaded: %dx%d", sprite.width, sprite.height)

    frames = split_sprite_sheet(sprite)
    logger.debug("Split into %d frames", len(frames))

    # Replace AI-generated blink frames with programmatic composites
    # for consistent eye-closed animation (AI blink frames have too much variation)
    if len(frames) >= 6:
        sleep_frame = frames[5]
        frames[2] = create_blink_frame(frames[0], sleep_frame)
        frames[3] = create_blink_frame(frames[1], sleep_frame)
        logger.info("Replaced blink frames 2,3 with programmatic composites from idle+sleep")

    # Crop all frames to a shared union bbox so every frame has identical
    # dimensions — keeps the character position stable across frames and
    # strips the empty margins the LLM leaves around the subject.
    frames = crop_frames_to_common_bbox(frames)
    if frames:
        logger.debug("Union-cropped frames to %dx%d", frames[0].width, frames[0].height)

    # Save raw PNG frames at full resolution for runtime scaling
    for i, frame in enumerate(frames):
        save_png_frame(config.pet_data_dir, pet.name, i, frame)

    return frames, sprite_path


def _generate_gemini_sprite_frames_10(config: TpetConfig, pet: PetProfile) -> tuple[list[Image.Image], Path]:
    """Generate and split a 10-frame (2x5) Gemini sprite sheet for macos-desktop mode.

    Uses the extended :func:`build_sprite_prompt_10` prompt and a taller
    aspect ratio (9:16 — the tallest portrait Gemini supports; 2:5 is not
    supported by the API). Splits via ``split_sprite_sheet`` which
    auto-detects the 2x5 layout by aspect ratio.
    """
    from tpet.art.client import GeminiImageClient
    from tpet.art.process import remove_chroma_key, split_sprite_sheet

    art_dir = get_art_dir(config.pet_data_dir)
    sprite_path = art_dir / f"{_sanitize_name(pet.name)}_sprite.png"

    resolved = config.resolved_image_art_provider
    client = GeminiImageClient(model=resolved.model, api_key=resolved.api_key)
    prompt = build_sprite_prompt_10(pet, prompt_override=config.art_prompt)
    save_prompt(config.pet_data_dir, pet.name, prompt)
    logger.debug("=== Gemini sprite prompt (10-frame sheet) ===\n%s\n=== end prompt ===", prompt)
    client.generate_sprite(
        prompt=prompt,
        output_path=sprite_path,
        aspect_ratio="9:16",
        resolution="1k",
    )

    sprite = Image.open(sprite_path)
    logger.debug("Sprite sheet loaded (10-frame): %dx%d", sprite.width, sprite.height)

    frames = split_sprite_sheet(sprite, layout="2x5", inset_px=6)
    logger.debug("Split into %d frames", len(frames))
    if len(frames) != 10:
        raise RuntimeError(
            f"Expected 10-frame sprite sheet for macos-desktop, got {len(frames)} frames. "
            f"Sprite was {sprite.width}x{sprite.height}."
        )

    # Strip magenta chroma background from each cell. No compositing or
    # union-bbox cropping — every frame is the AI-generated cell as-is, with
    # transparent background. Bias detection toward pure magenta so frames
    # where the cat touches a cell edge don't pick up the black outline as
    # the bg color, and use a generous tolerance to catch antialiased fringes.
    chroma_target = (255, 0, 255)
    chroma_tolerance = max(config.chroma_tolerance, 100)
    frames = [
        remove_chroma_key(f, tolerance=chroma_tolerance, target_color=chroma_target)
        for f in frames
    ]

    for i, frame in enumerate(frames):
        save_png_frame(config.pet_data_dir, pet.name, i, frame)

    return frames, sprite_path


def generate_macos_desktop_art_openai(
    config: TpetConfig,
    pet: PetProfile,
    on_progress: Callable[[int, int, str], None] | None = None,
    base_image_path: Path | None = None,
) -> tuple[list[str], GenerationResult | None]:
    """Generate the 10 PNG frames for macos-desktop via OpenAI/OpenRouter.

    Uses the parameterized OpenAI pipeline with :data:`_EDIT_PROMPTS_10`
    (adds walk-a, walk-b, fall, stunned to the existing idle-shift/react/sleep
    edits). Frames 2 and 3 remain programmatic blink composites.

    Saves each frame as ``{pet_name}_frame_{N}.png`` via save_png_frame — the
    OpenAI path already writes to those paths during the edit loop, but we
    save again to be consistent with the Gemini path.

    Returns the list of saved PNG paths (as strings) so callers can detect
    success and report the frame count.
    """
    frames, result = _generate_openai_frames(
        config,
        pet,
        on_progress=on_progress,
        base_image_path=base_image_path,
        edit_prompts=_EDIT_PROMPTS_10,
        frame_count=10,
    )

    # Frames are already on disk (the edit loop writes each one). Re-save to
    # be explicit, matching the Gemini path's behavior.
    paths: list[str] = []
    for i, frame in enumerate(frames):
        path = save_png_frame(config.pet_data_dir, pet.name, i, frame)
        paths.append(str(path))

    return paths, result


def generate_macos_desktop_art(
    config: TpetConfig,
    pet: PetProfile,
    on_progress: Callable[[int, int, str], None] | None = None,
    base_image_path: Path | None = None,
) -> tuple[list[str], GenerationResult | None]:
    """Generate the 10 PNG frames required by art_mode=macos-desktop.

    Routes to the Gemini sprite-sheet pipeline or the OpenAI edit pipeline
    based on the configured image art provider. Returns empty art-strings —
    the desktop pet consumes raw PNGs, not half-block text.
    """
    from tpet.art.openai_client import GenerationResult  # noqa: F401  # re-exported for type
    from tpet.config import LLMProvider

    resolved = config.resolved_image_art_provider

    if resolved.provider == LLMProvider.GEMINI:
        if base_image_path is not None:
            raise ValueError(
                "--base-image is not supported for macos-desktop with Gemini. "
                "Use OpenAI/OpenRouter provider if you need to supply your own idle frame."
            )
        _generate_gemini_sprite_frames_10(config, pet)
        return [], None

    if resolved.provider in (LLMProvider.OPENAI, LLMProvider.OPENROUTER):
        return generate_macos_desktop_art_openai(config, pet, on_progress=on_progress, base_image_path=base_image_path)

    raise ValueError(
        f"macos-desktop art generation is not supported for provider "
        f"{resolved.provider.value}. Use 'gemini', 'openai', or 'openrouter'."
    )


def _process_frames(
    frames: list[Image.Image],
    config: TpetConfig,
    pet_name: str,
    resize_fn: Callable[[Image.Image, int], Image.Image],
    target_height: int,
    convert_fn: Callable[[Image.Image], str],
    save_fn: Callable[[Path, str, int, str], Path] | None,
    label: str,
    chroma_key: bool = False,
    save_png: bool = False,
) -> list[str]:
    """Shared pipeline: resize -> optional chroma key -> convert -> save for each frame.

    Args:
        frames: Source PIL images.
        config: Application configuration.
        pet_name: Pet name for storage.
        resize_fn: Function to resize a frame (takes image and target_height).
        target_height: Target pixel height for resizing.
        convert_fn: Function to convert a PIL image to a text string.
        save_fn: Function to save a text frame (config_dir, pet_name, index, data), or None to skip.
        label: Human-readable frame type for logging.
        chroma_key: Whether to apply chroma key background removal after resize.
        save_png: Whether to also save source frames as raw PNGs.

    Returns:
        List of converted frame strings.
    """
    from tpet.art.process import remove_chroma_key

    output_frames: list[str] = []
    for i, frame in enumerate(frames):
        if save_png:
            save_png_frame(config.pet_data_dir, pet_name, i, frame)
        resized = resize_fn(frame, target_height)
        if chroma_key:
            resized = remove_chroma_key(resized, tolerance=config.chroma_tolerance)
        frame_data = convert_fn(resized)
        output_frames.append(frame_data)
        if save_fn:
            save_fn(config.pet_data_dir, pet_name, i, frame_data)
        logger.debug("%s frame %d: %dx%d -> %d chars", label, i, resized.width, resized.height, len(frame_data))
    return output_frames


def generate_halfblock_art(config: TpetConfig, pet: PetProfile) -> list[str]:
    """Generate half-block art frames via Gemini with chroma key removal.

    Args:
        config: Application configuration.
        pet: Pet profile.

    Returns:
        List of half-block strings (one per frame), or empty list on failure.
    """
    from tpet.art.process import image_to_halfblock, resize_for_halfblock

    logger.info("Generating halfblock sprite sheet for %s via Gemini", pet.name)
    frames, _ = _generate_gemini_sprite_frames(config, pet)
    return _process_frames(
        frames,
        config,
        pet.name,
        resize_fn=resize_for_halfblock,
        target_height=config.halfblock_size,
        convert_fn=image_to_halfblock,
        save_fn=save_halfblock_frame,
        label="Halfblock",
        chroma_key=True,
    )


