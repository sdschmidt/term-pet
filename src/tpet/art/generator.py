def _sprite_layout_instructions() -> str:
    """Return the frame layout and style instructions appended to all prompts.

    These instructions describe the 2x3 sprite sheet format and technical
    requirements for proper frame splitting. They are always included,
    regardless of any user-provided prompt override.

    Returns:
        Frame layout and style instruction string.
    """
    return (
        "Layout: 2 columns x 3 rows grid (6 panels). The overall image must be portrait orientation "
        "(taller than wide, approximately 2:3 aspect ratio). "
        "Each panel shows a different animation frame:\n"
        "- Row 1, Left: Idle pose (relaxed, eyes fully open)\n"
        "- Row 1, Right: Idle shift (slight pose variation, eyes fully open)\n"
        "- Row 2, Left: BLINK — pixel-for-pixel IDENTICAL to Row 1 Left (same body, same size, "
        "same position, same colors, same outline) with ONE change: the eyes are closed "
        "(same closed-eye style as the sleeping frame). Nothing else changes.\n"
        "- Row 2, Right: BLINK — pixel-for-pixel IDENTICAL to Row 1 Right (same body, same size, "
        "same position, same colors, same outline) with ONE change: the eyes are closed "
        "(same closed-eye style as the sleeping frame). Nothing else changes.\n"
        "- Row 3, Left: Surprised/reacting (eyes wide, mouth open)\n"
        "- Row 3, Right: Sleeping (eyes closed, zzz bubbles)\n\n"
        "Style: Clean pixel art, cute and charming, suitable for a terminal pet. "
        "The creature must look identical across all 6 frames (same proportions, colors, style). "
        "Only the expression and small pose details should differ between frames.\n\n"
        "The creature MUST have a thick black outline around the entire body and all major features. "
        "NO shadows, NO drop shadows, NO ambient occlusion. "
        "Flat solid colors only with bold black outlines."
    )


def _sprite_layout_instructions_10(pet: PetProfile) -> str:
    """Return the 10-frame (2 cols x 5 rows) layout instructions for macos-desktop mode.

    Walk / fall / landing rows are filled in from the pet's per-creature
    locomotion descriptors (``body_plan``, ``walk_description``,
    ``fall_description``, ``landing_description``) generated by the profile
    LLM, so a 4-legged cat, a centipede, a snail, and a balloon each get
    body-appropriate frames instead of a single hardcoded rule.
    """
    body_plan = pet.body_plan.strip() or f"a {pet.creature_type}"
    walk = pet.walk_description.strip() or (
        "Two-stride walk cycle facing right; pose A and pose B differ in stride only, "
        "no horizontal mirroring."
    )
    fall = pet.fall_description.strip() or (
        "Airborne with limbs splayed and a startled expression — no ground under it."
    )
    landing = pet.landing_description.strip() or (
        "Dazed on the ground: eyes as two small X shapes, 2-3 small yellow stars around its head, "
        "mouth slightly open."
    )

    return (
        "CRITICAL FORMATTING RULES (read these first):\n"
        "1. NO GRID LINES, NO BORDERS, NO PANEL OUTLINES, NO FRAME SEPARATORS, NO DIVIDERS. "
        "The 10 cells must be visually contiguous — only the magenta background and the "
        "characters appear. Do NOT draw any line, stroke, or border between cells.\n"
        "2. NO LABELS, NO TEXT, NO ROW/COLUMN NUMBERS, NO CAPTIONS anywhere on the image.\n"
        "3. The character MUST appear at the SAME SIZE in every cell — same body proportions, "
        "same scale, same number of pixels tall and wide. Do NOT zoom in for some frames or "
        "out for others. The bounding box of the character should be roughly identical across "
        "all 10 cells.\n"
        "4. Center the character horizontally within its cell, with consistent vertical placement "
        "(idle/walk: feet near the cell bottom; fall: mid-cell; landing: floor of the cell).\n\n"
        "Layout: 2 columns x 5 rows grid (10 panels). The overall image must be portrait "
        "orientation (taller than wide, approximately 2:5 aspect ratio). "
        f"The character has this anatomy: {body_plan}. Use this body plan consistently across "
        "every cell.\n"
        "Each panel shows a different animation frame:\n"
        "- Row 1, Left: Idle pose (relaxed, eyes fully open, on the ground)\n"
        "- Row 1, Right: Idle shift (same pose with tiny body/head tilt, eyes fully open)\n"
        "- Row 2, Left: BLINK — pixel-for-pixel IDENTICAL to Row 1 Left (same body, same size, "
        "same position, same colors, same outline) with ONE change: the eyes are closed "
        "(same closed-eye style as the sleeping frame). Nothing else changes.\n"
        "- Row 2, Right: BLINK — pixel-for-pixel IDENTICAL to Row 1 Right (same body, same size, "
        "same position, same colors, same outline) with ONE change: the eyes are closed "
        "(same closed-eye style as the sleeping frame). Nothing else changes.\n"
        "- Row 3, Left: Excited / surprised — eyes wide open, mouth open in a happy or "
        "startled expression. Same body and colors as idle.\n"
        "- Row 3, Right: Sleeping — eyes closed, tiny zzz bubbles above the head. "
        "The character may lay down if it fits the description.\n"
        f"- Row 4, Left: Walk stride A. {walk} Eyes open, FACING TO THE RIGHT.\n"
        "- Row 4, Right: Walk stride B — FACING TO THE RIGHT, the SAME direction as Row 4 Left. "
        "Do NOT flip or mirror the character horizontally. This is the mid-stride pose where the "
        "legs CROSS each other as they pass: limbs on opposite sides of the body swap which is "
        "forward, so only ONE leg per side is visible (the others are hidden behind the body). "
        "For creatures with many legs, show the wave pattern at its crossover point. For wingless "
        "or legless locomotion (slither, hover, slide), show the alternate phase of the same cycle.\n"
        f"- Row 5, Left: Falling. {fall} Keep the same character, same size, same style, same colors.\n"
        f"- Row 5, Right: Landing. {landing} Keep the same character, same colors, same style.\n"
        "\n"
        "Style: Clean pixel art, cute and charming, suitable for a terminal pet. "
        "The creature must look identical across all frames (same proportions, colors, style). "
        "Only the expression and pose should differ between frames.\n\n"
        "The creature MUST have a thick black outline around the entire body and all major features. "
        "NO shadows, NO drop shadows, NO ambient occlusion. "
        "Flat solid colors only with bold black outlines."
        "\n\n"
        "IMPORTANT:\n"
        "NO LABELS\n"
        "Don't label the generated image or the individual frames\n"
        "Don't separate the individual frames at all -- no outline, no grid"
    )


def build_sprite_prompt_10(pet: PetProfile, prompt_override: str = "") -> str:
    """Build the prompt for generating a 2x5 (10-frame) sprite sheet with chroma key background.

    Used when art is being generated for ``art_mode = macos-desktop``. Same
    background rules as :func:`build_sprite_prompt` (magenta chroma key for
    Gemini) but with the extended 10-frame layout.
    """
    subject = prompt_override.strip() if prompt_override.strip() else _default_subject(pet)
    return (
        f"{subject}\n\n"
        f"{_sprite_layout_instructions_10(pet)}\n\n"
        "Clean edges, no anti-aliasing into background.\n\n"
        "CRITICAL: The background MUST be solid pure magenta #FF00FF RGB(255,0,255) "
        "with absolutely no gradients, no shadows, no shading, no variations. "
        "A single flat magenta color across the entire background. "
        "Isolated subject, centered composition within each cell. "
        "This is essential for automated background removal processing."
    )


def _default_subject(pet: PetProfile) -> str:
    """Return the default subject description from pet profile data.

    Args:
        pet: Pet profile with creature info.

    Returns:
        Subject description string.
    """
    return (
        f"Create a pixel art sprite sheet of a {pet.creature_type} named {pet.name}. "
        f"Personality: {pet.personality}. "
        f"Rarity: {pet.rarity.value}."
    )


def build_sprite_prompt(pet: PetProfile, prompt_override: str = "") -> str:
    """Build the prompt for generating a 2x3 sprite sheet with chroma key background.

    For use with Gemini (no native transparency). Requests solid magenta
    background for automated chroma key removal.

    If prompt_override is provided, it replaces the default subject description
    but the frame layout instructions and background requirements are always appended.

    Args:
        pet: Pet profile with creature info.
        prompt_override: Custom prompt to use instead of auto-generated subject.

    Returns:
        Prompt string for Gemini image generation.
    """
    subject = prompt_override.strip() if prompt_override.strip() else _default_subject(pet)
    return (
        f"{subject}\n\n"
        f"{_sprite_layout_instructions()}\n\n"
        "Clean edges, no anti-aliasing into background.\n\n"
        "CRITICAL: The background MUST be solid pure magenta #FF00FF RGB(255,0,255) "
        "with absolutely no gradients, no shadows, no shading, no variations. "
        "A single flat magenta color across the entire background. "
        "Isolated subject, centered composition. "
        "This is essential for automated background removal processing."
    )


def build_openai_sprite_prompt(pet: PetProfile, prompt_override: str = "") -> str:
    """Build the prompt for generating a 2x3 sprite sheet with transparent background.

    For use with OpenAI (native transparency support, no chroma key needed).
    Legacy: used when generating all frames in a single call.

    If prompt_override is provided, it replaces the default subject description
    but the frame layout instructions are always appended.

    Args:
        pet: Pet profile with creature info.
        prompt_override: Custom prompt to use instead of auto-generated subject.

    Returns:
        Prompt string for OpenAI image generation.
    """
    subject = prompt_override.strip() if prompt_override.strip() else _default_subject(pet)
    return (
        f"{subject}\n\n"
        f"{_sprite_layout_instructions()}\n\n"
        "The background must be completely transparent. "
        "Do NOT add any background color, gradient, or fill. "
        "The subject must be isolated with clean pixel-perfect edges."
    )


